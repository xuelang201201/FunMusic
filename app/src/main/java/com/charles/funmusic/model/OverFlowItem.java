package com.charles.funmusic.model;

public class OverFlowItem {

    /**
     * 信息标题
     */
    private String mTitle;
    /**
     * 图片id
     */
    private int mAvatar;

    public String getTitle() {
        return mTitle;
    }

    public void setTitle(String title) {
        mTitle = title;
    }

    public int getAvatar() {
        return mAvatar;
    }

    public void setAvatar(int avatar) {
        mAvatar = avatar;
    }
}
